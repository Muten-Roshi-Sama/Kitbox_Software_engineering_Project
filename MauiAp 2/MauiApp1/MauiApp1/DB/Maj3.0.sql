CREATE TABLE IF NOT EXISTS Components (
    Reference VARCHAR(30) NOT NULL,
    Code VARCHAR(10) NOT NULL,
    HeightC INT(6) DEFAULT 0,
    LengthC INT(6) DEFAULT 0,
    DepthC INT(6) DEFAULT 0,
    Color TINYINT DEFAULT 0,
    IdSupplier INT UNSIGNED NOT NULL,
    PriceSupplier FLOAT DEFAULT 0.00,
    DelaySupplier INT(3),
    StockAvailable INT(6) DEFAULT 0,
    StockReserved INT(6) DEFAULT 0,
    CONSTRAINT PK_Compo PRIMARY KEY(Code,IdSupplier)
);

CREATE TABLE IF NOT EXISTS CommandsSupplier (
    Reference VARCHAR(30) NOT NULL,
    Code VARCHAR(10) NOT NULL,
    IdSupplier INT UNSIGNED NOT NULL,
    Price FLOAT DEFAULT 0.00,
    Delay INT(3),
    Quantity INT(6) DEFAULT 0,
    Received BOOLEAN DEFAULT 0,
    CONSTRAINT PK_CompoSupp PRIMARY KEY(Code,IdSupplier),
    CONSTRAINT FK_ComSupp FOREIGN KEY(Code,IdSupplier) REFERENCES Components(Code,IdSupplier)
);

